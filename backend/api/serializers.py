class RecipeWriteSerializer(ModelSerializer):
    tags = PrimaryKeyRelatedField(queryset=Tag.objects.all(),
                                  many=True)
    author = CustomUserSerializer(read_only=True)
    ingredients = IngredientInRecipeWriteSerializer(many=True)
    image = Base64ImageField()

    class Meta:
        model = Recipe
        fields = (
            'id',
            'tags',
            'author',
            'ingredients',
            'name',
            'image',
            'text',
            'cooking_time',
        )

    def representation(self, instance):
        request = self.context.get('request')
        context = {'request': request}
        return RecipeReadSerializer(instance,
                                    context=context).data

    @staticmethod
    def validate_tags(value):
        tags = value
        if not tags:
            raise ValidationError({
                'tags': 'Add at least one tag'
            })
        tags_list = []
        for tag in tags:
            if tag in tags_list:
                raise ValidationError({
                    'tags': 'Tags must be unique'
                })
            tags_list.append(tag)
        return value

    @staticmethod
    def validate_ingredients(value):
        ingredients = value
        if not ingredients:
            raise ValidationError({
                'ingredients': 'Add at least one ingredient'
            })
        ingredients_list = []
        for item in ingredients:
            ingredient = get_object_or_404(Ingredient, id=item['id'])
            if ingredient in ingredients_list:
                raise ValidationError({
                    'ingredients': 'Ingredients must be unique'
                })
            if int(item['amount']) <= 0:
                raise ValidationError({
                    'amount': 'Count must be greater than 0'
                })
            ingredients_list.append(ingredient)
        return value

    @staticmethod
    @transaction.atomic
    def _create_quantity_of_ingredients(ingredients, recipe):
        IngredientInRecipe.objects.bulk_create(
            [IngredientInRecipe(
                ingredient=get_object_or_404(Ingredient, id=ingredient['id']),
                recipe=recipe,
                amount=ingredient['amount']
            ) for ingredient in ingredients]
        )

    @transaction.atomic
    def update(self, instance, validated_data):
        tags = validated_data.pop('tags')
        ingredients = validated_data.pop('ingredients')
        instance = super().update(instance, validated_data)
        instance.tags.clear()
        instance.tags.set(tags)
        instance.ingredients.clear()
        self.create_quantity_of_ingredients(
            recipe=instance,
            ingredients=ingredients
        )
        instance.save()
        return instance

    @transaction.atomic
    def create(self, validated_data):
        tags = validated_data.pop('tags')
        ingredients = validated_data.pop('ingredients')
        recipe = Recipe.objects.create(**validated_data)
        recipe.tags.set(tags)
        self.create_quantity_of_ingredients(
            recipe=recipe,
            ingredients=ingredients
        )
        return recipe


